#include <iostream>
using namespace std;
int main(){
    double a,b;
    cout<<"enter a: ";
    cin >> a;
    cout<<"enter b: ";
    cin>>b;
    cout<<"sum of "<<a<<" and "<<b<<" is: "<<a+b<<endl;
    cout<<"product of "<<a<<" and "<<b<<" is: "<<a*b<<endl;
    cout<<"difference of "<<a<<" and "<<b<<" is: "<<a-b<<endl;
    cout<<"divide of "<<a<<" and "<<b<<" is: "<<a/b<<endl;
    return 0;
}